#include<stdio.h>
int main()
{
	int n,i,j;
	scanf("%d",&n);
	for(i=1;i<=n;i++)
	{
		if(i%2==0)
		{
			for(j=0;j<6;j++)
			{
				if(j==0)
					printf("%d",i+1);
				else
					printf("%d",i);
			}
		}
		else
		{
			for(j=0;j<6;j++)
			{
				if(j==5)
					printf("%d",i+1);
				else
					printf("%d",i);
			}	
		}
		printf("\n");
	}
}
